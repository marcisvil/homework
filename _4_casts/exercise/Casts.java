package _4_casts.exercise;

public class Casts {
	public static void main(String[] args) {
		// Create a program that will cast float 3.14 and double 3.14 to all available primitives.

		System.out.println("(double)3.14F = " + (double) 3.14F);
		System.out.println("(long)3.14F =   " + (long) 3.14F);
		System.out.println("(int)3.14F =    " + (int) 3.14F);
		System.out.println("(char)3.14F =   " + (char) 3.14F);
		System.out.println("(short)3.14F =  " + (short) 3.14F);
		System.out.println("(float)3.14F =  " + (float) 3.14F);
		System.out.println();
		System.out.println("(double)3.14d = " + (double) 3.14d);
		System.out.println("(long)3.14d =   " + (long) 3.14d);
		System.out.println("(int)3.14d =    " + (int) 3.14d);
		System.out.println("(char)3.14d =   " + (char) 3.14d);
		System.out.println("(short)3.14d =  " + (short) 3.14d);
		System.out.println("(float)3.14d =  " + (float) 3.14d);
	}
}
